const funcMap = { zeta, altZeta, primeZeta };

describe('High-Precision Zeta Function Table-Driven Tests', () => {

    beforeAll(async () => {
        await init();
    });

    test.each(testData)('$desc (Case $id)', ({ func, args, expected, prec }) => {
        const targetFunc = funcMap[func];
        
        const processedArgs = args.map(arg => {
            return (arg.im === "0") ? bf(arg.re) : complex(arg.re, arg.im);
        });

        const result = targetFunc(...processedArgs);

        if (expected.re === "Infinity") {
            expect(result.re.toString()).toBe("Infinity");
            return;
        }

        const expectedVal = complex(expected.re, expected.im);
        
        expectBFCloseTo(result.re, expectedVal.re, prec);
        expectBFCloseTo(result.im, expectedVal.im, prec);
    });

    test('Symmetry check: zeta(s) == conj(zeta(conj(s)))', () => {
        const s = complex(0.5, "14.1347251417347");
        const s_conj = complex(0.5, "-14.1347251417347");
        const ex=zeta(s);
        const exconj=zeta(s_conj).conj();
        expectBFCloseTo(ex.re, exconj.re, 40);
        expectBFCloseTo(ex.im, exconj.im, 40);
    });
});